 u32 HashString(u8ptr lpszString, u32 dwHashType)
 {
     u8ptr key = lpszString;
     u32ptr ht = (u32ptr)HashTable;
     u32 seed1 = 0x7FED7FED, seed2 = 0xEEEEEEEE;
     s32 ch = '\x00';
 
     while(*key != 0)
     {   
         ch = *key++;
 
         seed1 = ht[(dwHashType << 8) + ch] ^ (seed1 + seed2);
         seed2 = ch + seed1 + seed2 + (seed2 << 5) + 3;
     }  

     return seed1;
 }

